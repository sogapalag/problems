template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

template <typename T=int>
struct Fenwick {
    int n;
    vector<T> v;
    
    Fenwick(int size = 100005) : n(size), v(n+1, 0) {}
    inline void add(int p, T val) {
        for (; p <= n; p += (p&-p)) { v[p] += val; }
    }
    inline T query(int p) {
        T tmp = 0;
        for (; p > 0; p -= (p&-p)) { tmp += v[p]; }
        return tmp;
    }
    inline T query(int l, int r) {
        return query(r) - query(l-1);
    }
};

// for a (u,v) f.query := # ready move right to -> u 
// so for those less v, potential different up move, pref-sum
void solve() {
    int n,m;
    cin >> n >> m;
    vector<int> x(n);
    vector<int> y(m);
    for (auto& i: x) {
        cin >> i;
    }
    for (auto& i: y) {
        cin >> i;
    }
    vector<pair<int, int>> a;
    vector<int> Y;
    for (int i: x){
        if (i < y[0] || i > y[m-1]) continue;
        int p = lower_bound(y.begin(), y.end(), i) - y.begin();
        a.emplace_back(i - y[p-1], i - y[p]); // for down process
        Y.emplace_back(y[p] - i);
    }
    sort(a.begin(), a.end());
    a.erase(unique(a.begin(), a.end()), a.end());
    sort(Y.begin(), Y.end());
    Y.erase(unique(Y.begin(), Y.end()), Y.end());
    n = Y.size();
    Fenwick<mint> f(n+5);
    f.add(1, 1);
    for (auto& c: a) {
        int p = lower_bound(Y.begin(), Y.end(), -c.second) - Y.begin();
        p += 2;
        f.add(p, f.query(p-1));
    }
    cout << f.query(n+5);
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
