template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

mint dp[51][51][2512];

// https://oeis.org/A062869
// consider a bi-p graph,
// left 1,2,..n, right p(1),...p(n).
// total displacement = 2\sum max(i-p_i, 0). = 
// for the graph, we connect each i, aka, i - p(j)=i.
// then td also equal to those line bias. or imagine n-1 horit. line, cross thoses line, #point = td;
//
// dp[i][j][k] = in first ith node, there are j(<=i) reserved(undraw), and td=k.
//
// first a simple case
// 1 1
// 2 2
// then for 3 3, there're 4 kinds connect, whether consume 3_left  or not, whether consume 3_right or not.
// for next still reserve j. either 3-3, or consume 3_l to right reserve, or consume 3_r to left reserve.
//  so 2*j + 1 ways.
// for next reserve j+1, 1 way.
// for next reserve j-1. 3_l, 3_r consume with different else node. j*j ways.
//
// then a key trick.
// whether we draw/consume or not. we make new val v+2*j. (hard to think/prove) 
// which means the effect just as 3_l-1_r, 3_r-1_l.
// since for future potential s_l - 1_r, swap with 3_l, the td same. so always treat after scheme reserve as closest rectangle. aka 1,1; 2,2;...;j,j
//
// imagine as potential energy.
void solve() {
    int n, k;
    cin >> n >> k;
    memset(dp, 0, sizeof dp);
    dp[0][0][0] = 1;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j <= i; j++) {
            for (int v = 0; v+2*j <= k; v++) {
                mint& num = dp[i][j][v];
                if(j) dp[i+1][j-1][v+2*j] += num * j*j;
                dp[i+1][j][v+2*j] += num * (2*j+1);
                dp[i+1][j+1][v+2*j] += num;
            }
        }
    }
    cout << dp[n][0][k];
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
