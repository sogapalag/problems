typedef modular<int> mint;

struct Comb {
    int N;
    vector<mint> fac, ifac, inv;

    Comb(int _n=1) {
        resize(2);
        fac[0] = fac[1] = ifac[0] = ifac[1] = inv[1] = 1;
        N = 1; grow(_n);
    }

    void resize(int _s) {
        fac.resize(_s); ifac.resize(_s); inv.resize(_s);
    }
    bool grow(int _n) {
        if (_n <= N) return false;
        swap(_n, N); resize(N+1);
        for (int i = _n+1; i <= N; i++) {
            fac[i] = fac[i-1] * i;
            inv[i] = (-MOD/i) * inv[MOD%i];
            ifac[i] = ifac[i-1] * inv[i];
        }
        return true;
    }
    // perm
    mint a(int n, int k) {
        if (n < k || k < 0) return 0;
        if (n > N) grow(n);
        return fac[n] * ifac[n-k];
    }
    // choose
    mint c(int n, int k) {
        if (n < k || k < 0) return 0;
        if (n > N) grow(n);
        return fac[n] * ifac[n-k] * ifac[k];
    }
}c(5123);

// consider some edge (u,v), can be use at most 2*min(sz(u),sz(v)).
// <=> 
// for two cds, (i,p_i) has to pass (u,v)
// for one cd, (i,p_i) has to pass cd
void solve() {
    int n; cin >> n;
    vector<vector<int>> g(n);
    for (int _ = 1; _ < n; _++) {
        int x, y;
        cin >> x >> y;
        x--;y--;
        g[x].emplace_back(y);
        g[y].emplace_back(x);
    }
    // centroid decomp
    vector<int> pa(n, -1);
    vector<int> sz(n, 1);
    function<void(int,int)> dfs = [&](int u,int p){
        for (int v:g[u])if(v!=p){
            pa[v] = u;
            dfs(v,u);
            sz[u] += sz[v];
        }
    };
    dfs(0,-1);
    vector<int> cds;
    for (int i = 0; i < n; i++) {
        if (all_of(g[i].begin(), g[i].end(), [&](int v){
                    if (v!=pa[i]) return sz[v] <= n/2;
                    else return n-sz[i] <= n/2;
                    })) {
            cds.emplace_back(i);
        }
    }

    assert(cds.size() <= 2);
    if (cds.size() == 2) {
        assert(n%2 == 0);
        mint res = c.fac[n/2] * c.fac[n/2];
        cout << res; return;
    }
    int cd = cds[0];
    vector<int> sub;
    for (int v: g[cd]) {
        if (v != pa[cd]) {
            sub.emplace_back(sz[v]);
        }else{
            sub.emplace_back(n-sz[cd]);
        }
    }
    vector<mint> dp(n); // in before groups, i in, another j items map to same sub
    dp[0] = 1;
    int sum = 0;
    for (int x: sub) {
        for (int i = sum; i >= 0; i--) {
            for (int j = x; j >= 1; j--) {
                dp[i+j] += dp[i] * c.c(x, j)*c.a(x,j);
            }
        }
        sum += x; 
    }
    // iep, i items map to same sub
    mint res = 0;
    for (int i = 0; i < n; i++) {
        mint tmp = dp[i] * c.fac[n-i]; // n-i-1 items has n-i ! perm, since (cd) can be pos
        (i&1)?(res-=tmp):(res+=tmp);
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
