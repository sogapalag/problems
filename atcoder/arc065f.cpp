const int N = 3003;
mint dp[N][N]; // i fixed, j ones could use for next print

void solve() {
    int n,m;
    cin >> n >> m;
    string s; cin >> s;
    vector<int> sum(n+2);
    sum[0] = 0;
    for (int i = 0; i < n; i++) {
        sum[i+1] = sum[i] + (s[i]-'0');
    }
    sum[n+1] = sum[n];
    vector<int> R(n+2);
    iota(R.begin(), R.end(), 0);
    for (int _ = 0; _ < m; _++) {
        int l, r;
        cin >> l >> r;
        R[l] = max(R[l], r);
    }
    for (int i = 1; i <= n+1; i++) {
        R[i] = max(R[i], R[i-1]);
    }
dbg(sum, R);
    dp[0][sum[R[1]]] = 1;
    for (int i = 1; i <= n; i++) {
        for (int j = 0; j <= n; j++) {
            // available now
            int one = j, zero = R[i] - (i-1) - j;
            // new resource
            int l = R[i], r = R[i+1];
            int new_one = sum[r]-sum[l];
            // print(use) 1
            if (one > 0) dp[i][one-1 + new_one] += dp[i-1][j];
            // print(ues) 0
            if (zero > 0) dp[i][one  + new_one] += dp[i-1][j];
        }
    }
    cout << dp[n][0];
}

