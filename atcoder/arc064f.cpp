template <typename T, typename U> inline bool operator!=(const modular<T>& _lhs, const U& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;


// if shift p, palin again, let first (p)block as <
//   then must be form  < > < > ... < >, 
//   if #< + #> odd, then < itself must be <>
// so now, we can only consider block d=<>
//   K^{(d+1)/2} ways, - f(d'|d)
// for shift, note if d even, <>, >< count twice
void solve() {
    int n, k;
    cin >> n >> k;
    vector<int> d;
    for (int i = 1; i*i <= n; i++) {
        if (n%i) continue;
        d.emplace_back(i);
        if (i*i!=n) d.emplace_back(n/i);
    }

    sort(d.begin(), d.end());
    map<int,mint> f;
    int m = d.size();
    for (int i = 0; i < m; i++) {
        mint ways = mint(k).exp((d[i]+1)/2);
        for (int j = 0; j < i; j++) {
            if (d[i] % d[j] == 0) {
                ways -= f[d[j]];
            }
        }
        f[d[i]] = ways;
    }
    
    mint res = 0;
    for (auto& p: f) {
        int x; mint w;
        tie(x,w) = p;
        if (x & 1) res += w * x;
        else res += w * (x/2);
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
