typedef modular<int> mint;

const int N = 606; 
mint dp[N][N]; // pair [i..j) a scc_ij ways when consider only [i..j)

// count scc_ij min=i,max=j
void solve() {
    int n,m;
    cin >> n >> m;
    const int T = 2*n-2*m; 
    vector<int> mat(2*n, -1);
    vector<int> a(2*n+1, 1); a[0] = 0;
    for (int _ = 0; _ < m; _++) {
        int x,y;
        cin >> x >> y;
        a[x] = a[y] = 0;
        x--;y--;
        mat[x] = y;
        mat[y] = x;
    }
    m = 2*n;
    for (int i = 1; i <= m; i++) {
        a[i] += a[i-1];
    }
    vector<mint> g(m+1); g[0] = 1;
    for (int i = 1; i <= m; i++) {
        g[i] = (i&1)? 0: g[i-2]*(i-1);
    }
    
    mint res = 0;
    for (int d = 2; d <= m; d++) {
        for (int i = 0; i+d <= m; i++) {
            int j = i+d;
            int cnt = a[j] - a[i];
            int out = T-cnt;
            // in con. out
            for (int k = i; k < j; k++) {
                if (mat[k]!=-1 && (mat[k]<i || mat[k]>=j)) goto nxt;
            }
            dp[i][j] = g[cnt];
            for (int k = i+2; k < j; k++) {
                cnt = a[j] - a[k];
                dp[i][j] -= dp[i][k] * g[cnt];
            }
            res += dp[i][j] * g[out];
            nxt:;
        }
    }
    
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
