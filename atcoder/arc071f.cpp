typedef modular<int> mint;

// note blocks 211, 3111, end xxxxxx, consist of with these with insert 1s notin xxxx. valid seq.
// so dp[i] end with a finished block, end with 1.
//
// editorial has simple method, dp[i]: a[i..] all equal.
//  by consider a[1]=1, brand new dp[i-1]
//              a[1]=x, a[2]==1, dp[i-x-1] /  n-x+1, x>i
//              a[1]=x, a[2]!=1, (n-1)^2
void solve() {
    int n; cin >> n;
    vector<mint> dp(n+1);
    dp[0] = 1;
    mint sum = 1;
    for (int i = 1; i <= n; i++) {
        dp[i] = sum - (i>1?dp[i-2]:0);
        sum += dp[i];
    }
    mint res = dp[n];
    sum -= dp[n] + dp[n-1];
    res += dp[n-1] * (n-1); // a_n=x, xxx
    res += sum * (n-1)*(n-1); // xyyyyyy
    for (int i = 2; i <= n; i++) {
        res += dp[n-i] * (n-i+1); // end with 1
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
