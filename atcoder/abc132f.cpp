template <typename T> inline bool operator!=(const modular<T>& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const modular<T>& _lhs, const U& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

const int SQP = 31666; 
const int N = 101; 

mint a[N][SQP], b[N][SQP];

// https://codeforces.com/blog/entry/68031#comment-523597
// https://www.hackerrank.com/challenges/p-sequences/editorial
//
// a[k][i] length k, last <= i
// b[k][i] length k, last <= p/i
//
// a[k][i] = b[k][p/i], and key part notice, b's relation, delta(b): means last in (p/(i+1), p/i] so k-1's element exactly iff <=i 
void solve() {
    int p, n; cin >> p >> n;
    int sqp = sqrt(p)+1;
    for (int i = 1; i <= sqp; i++) {
        a[1][i] = i;
        b[1][i] = p/i;
    }
    for (int k = 2; k <= n; k++) {
        a[k][0] = 0;
        for (int i = 0; i < sqp; i++) {
            a[k][i+1] = a[k][i] + b[k-1][i+1];
        }
        b[k][sqp] = a[k][p/sqp];
        for (int i = sqp-1; i > 0; i--) {
            b[k][i] = b[k][i+1] + a[k-1][i] * (p/i - p/(i+1));
        }
    }
    cout << b[n][1];
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
