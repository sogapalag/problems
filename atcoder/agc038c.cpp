template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

const int N = 1e6+10; 
int cnt[N];
mint w[N];

// key trick is construct w, s.t. sum_i|k wi = 1/k
void solve() {
    int n; cin >> n;
    for (int _ = 0; _ < n; _++) {
        int x; cin >> x;
        cnt[x]++;
    }
    for (int i = 1; i < N; i++) {
        w[i] += mint(1)/i;
        for (int p = 2*i; p < N; p+=i) {
            w[p] -= w[i];
        }
    }

    mint res = 0;
    for (int i = 1; i < N; i++) {
        mint sum = 0;
        mint s2 = 0;
        for (int p = i; p < N; p+=i) {
            sum += cnt[p] * mint(p);
            s2 += cnt[p] * mint(p) * p;
        }
        mint tmp = (sum*sum - s2) / 2;
        res += tmp * w[i];
    }
    cout << res;
}

// sum, s2 is same as above to calc G(d) = sum_d|g x*y
// note, G(d) = sum_g=d xy + sum_g=2d.. + ..3d...
// let F(d) = sum_g=d xy, here w[d]. once we get all F, sum F/d is our answer.
// apparently it could calc by G(d) - F(2d) - F(3d) -...
void solve2() {
    int n; cin >> n;
    for (int _ = 0; _ < n; _++) {
        int x; cin >> x;
        cnt[x]++;
    }
    mint res = 0;
    for (int i = N-1; i >= 1; i--) {
        mint sum = 0;
        mint s2 = 0;
        for (int p = i; p < N; p+=i) {
            sum += cnt[p] * mint(p);
            s2 += cnt[p] * mint(p) * p;
            w[i] -= w[p];
        }
        mint tmp = (sum*sum - s2) / 2;
        w[i] += tmp;
        res += w[i]/i;
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve2();
    cout << endl;
}
