template <typename T> inline bool operator!=(const modular<T>& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const modular<T>& _lhs, const U& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

const int N = 305; 
mint dp[N][N][N];

void solve() {
    int n,m;
    cin >> n >> m;
    vector<vector<pair<int,int>>> g(n+1);
    for (int _ = 0; _ < m; _++) {
        int l,r,x;
        cin >> l >> r >> x;
        g[r].emplace_back(l,x);
    }

    // slow ck
    auto ck = [&](vector<int> a){
        sort(a.begin(), a.end());
        for (auto& p: g[a[2]]) {
            int l,x;
            tie(l,x) = p;
            if (l>a[1]) {
                if (x!=1) return false;
            } else if (l>a[0]) {
                if (x!=2) return false;
            } else if (x!=3) return false;
        }
        return true;
    };

    dp[0][0][0] = 1;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j <= i; j++) {
            for (int k = 0; k <= j; k++) {
                dp[i+1][j][k] += dp[i][j][k];
                dp[i+1][i][k] += dp[i][j][k];
                dp[i+1][i][j] += dp[i][j][k];
            }
        }
        for (int j = 0; j <= i; j++) {
            for (int k = 0; k <= j; k++) {
                for (auto& p: g[i+1]) {
                    int l,x;
                    tie(l,x) = p;
                    if (x != 1 + (j>=l) + (k>=l)) dp[i+1][j][k] = 0;
                }
            }
        }
    }
    mint res = 0;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            res += dp[n][i][j];
        }
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
