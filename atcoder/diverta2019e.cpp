template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

// a[i]: pref ^xor sum; z[i]: cnt of zeros [1..i];
// note the problem, <=> start at a[0]=0, jump to some a[i]. thus form a sequence valid only if
//                        0->X->0->X->..->a[n](final stay).
//                      dp[i]:= stay with index i, #valid. thus dp[i] = \sum_j dp[j] * cnt_0 (j to i).
//                      thus def pref-sum su[i]:= \sum_j dp[j].
//                      dp[i]:= dp[j] + su[j]* (z[i]-z[j]);  (note, i,j are X's indexes, so store pre-array)
//                      note set(dp[0]=1, su[0]=0) since first (X) cannot take those (0s) make a trans
//
//                    when d[n] != 0, dp[n]'s definition is actually our answer.
//                    when d[n] == 0, just sum those dp[1..<n] (a[i]!=0), (i.e. direct to end)
//                                    plus, a 0->0->0-> chain, 2^(z[n]-1);
//  one can use BIT count zeros, can ues dp[x] instead of dp[i].
void solve() {
    int n; cin >> n;
    vector<int> a(n+1);
    vector<int> pre(n+1);
    vector<int> las(1<<20);
    vector<int> z(n+1);
    for (int i = 1; i <= n; i++) {
        int x; cin >> x;
        a[i] = a[i-1] ^ x;
        z[i] = z[i-1] + !a[i];
        pre[i] = las[a[i]];
        las[a[i]] = i;
    }
    vector<mint> dp(n+1); dp[0] = 1;
    vector<mint> su(n+1);
    for (int i = 1; i <= n; i++) {
        int j = pre[i];
        if (a[i]) {
            dp[i] = dp[j] + su[j] * (z[i]-z[j]);
            su[i] = su[j] + dp[i];
        } else {
            dp[i] = dp[j] * 2;    
        }
    }
    mint res;
    if (a[n]) {
        res = dp[n];
    } else {
        res = dp[n]/2;
        for (int i = 1; i < n; i++) {
            if (a[i]) {
                res += dp[i];
            }
        }
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
