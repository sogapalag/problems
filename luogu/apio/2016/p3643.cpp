const int N = 512; 
mint dp[N][2*N], sum[N][2*N];

// almost-same problem as cf1295f
void solve() {
    int n; cin >> n;
    vector<int> Xs;
    vector<int> l(n), r(n);
    for (int i = 0; i < n; i++) {
        cin >> l[i] >> r[i];
        ++r[i];
        Xs.push_back(l[i]);
        Xs.push_back(r[i]);
    }
    Xs.push_back(0);
    sort(Xs.begin(), Xs.end());
    Xs.erase(unique(Xs.begin(), Xs.end()), Xs.end());
    int m = Xs.size() - 1;
    vector<mint> inv(n + 1);
    for (int i = 1; i <= n; i++) {
        inv[i] = mint(1)/i;
    }
    for (int i = 0; i <= m; i++) {
        sum[0][i] = 1;
    }
    for (int i = 0; i < n; i++) {
        for (int j = 1; j < m; j++) {
            int x = Xs[j], y = Xs[j+1];
            mint way = 1;
            // way := \sum_{k=[1..cnt]} c(y-x, k) * c(cnt-1, k-1) // i.e. k=#use, fix(left-most must use)
            //      = c(y-x-1+cnt, cnt)  // recall sum c(r,k)c(s,n-k)=c(r+s,n)  //same formula as '<='
            int cnt = 0;
            for (int k = i; k >= 0; k--) {
                if (l[k] <= x && y <= r[k]) {
                    (way *= (y-x+cnt)) *= inv[cnt+1];
                    cnt++;
                    dp[i+1][j] += sum[k][j-1] * way;
                }
            }
        }
        sum[i+1][0] = 1; // 0..i all not used
        for (int j = 1; j <= m; j++) {
            sum[i+1][j] = sum[i+1][j-1] + dp[i+1][j];
dbg(i+1,j,sum[i+1][j],dp[i+1][j]);
        }
    }
    cout << sum[n][m]-1; // -1 
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
