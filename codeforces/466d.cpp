template <typename T> inline bool operator!=(const modular<T>& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const modular<T>& _lhs, const U& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

const int N = 2048; 
mint dp[N][N]; // till i, j=#<0, i.e. j=#-1.

void solve() {
    int n, h;
    cin >> n >> h;
    vector<int> a(n);
    for (auto& x: a) {
        cin >> x;
    }
    // diff
    vector<int> d(n+1);
    d[0] = a[0]; d[0] -= h;
    for (int i = 1; i < n; i++) {
        d[i] = a[i] - a[i-1];
    }
    d[n] = 0-a[n-1]; d[n] += h;

    // problem transformed to
    // make d -> all 0, by op: select, l<r, d[l]++, d[r]--. {l}/{r}occur no twice,
    for (int x: d) if (abs(x)>1) {
        cout << 0; return;
    }
    if (d[0]>0) {
        cout << 0; return;
    }
    dp[0][d[0]<0] = 1;
    // by i(r) order decide whether use (?,r)
    for (int i = 1; i <= n; i++) {
        int x = d[i];
        for (int j = 0; j <= i; j++) {
            // don't use op
            dp[i][j + (x<0)] += dp[i-1][j];
            // choose some -1 as l.
            if (x>=0&&j) {
                dp[i][j-1 + (x==0)] += dp[i-1][j] * j;
            }
        }
    }
    cout << dp[n][0];
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
