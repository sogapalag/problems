template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

const int N = 2048; 
mint dp[N][N]; // last not'=' pos, with j valid already.
mint sum[N]; // sum_i #j valid

// note for some t, c.to s, like  [<<==>><..=><..]
//   lexi.greater i.e. prefix is (=*)>, i.e. we focus on canonical first > constribution.
//     will be left extend by =, to some not =, right extend to end
// then a naive O(n^3)dp, but notice k~n, and (l*r) can be constraited, i.e. log(.)
void solve() {
    int n,k;
    cin >> n >> k;
    string s; cin >> s;
    dp[0][0] = 1; sum[0] = 1;
    for (int i = 0; i < n; i++) {
        int x = s[i] - 'a';
        // <
        for (int j = 0; j <= k; j++) {
            dp[i+1][j] += x*sum[j];
        }
        // >
        for (int l = 0; l <= i; l++) {
            for (int j = 0; j <= k && (i-l+1)*(n-i)+j <= k; j++) { // log
                dp[i+1][j + (i-l+1)*(n-i)] += (25-x) * dp[l][j];
            }
        }
        for (int j = 0; j <= k; j++) {
            sum[j] += dp[i+1][j];
        }
    }
    cout << sum[k];
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
