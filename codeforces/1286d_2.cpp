template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

template <typename M>
struct SegTree {
    using Op = function<M(const M&, const M&)>;
    M ID; // monoid identity
    Op op; // associative operation
    int N; // 2^lg space, for non-commutative
    vector<M> a;
    
    SegTree(int n, M leaf_default, M _ID, Op _op) : ID(_ID), op(_op) {
        init_space(n);
        fill(a.begin() + N, a.begin() + N + n, leaf_default);
        build();
    }
    SegTree(const vector<M> leaves, M _ID, Op _op) : ID(_ID), op(_op) {
        int n = leaves.size();
        init_space(n);
        copy(leaves.begin(), leaves.end(), a.begin() + N);
        build();
    }
    void init_space(int n) {
        N = 1; while (N < n) N<<=1;
        a.assign(N<<1, ID);
    }
    inline void pull(int i) { a[i] = op(a[i<<1], a[i<<1|1]); }
    void build() { for (int i = N-1; i >= 1; i--) pull(i); }

    void assign(int p, M val) {
        assert(0 <= p && p < N);
        for (a[p += N] = val; p >>= 1;) pull(p);
    }
    void cancel(int p, int x, int y) {
        for (a[p += N][x][y] = 0; p >>= 1;) pull(p);
    }
    M query(int l, int r) const {
        assert(0 <= l && r <= N);
        M resl(ID), resr(ID);
        for (l += N, r += N; l < r; l >>= 1, r >>= 1) {
            if (l & 1) resl = op(resl, a[l++]);
            if (r & 1) resr = op(a[--r], resr);
        }
        return op(resl, resr);
    }
    M query_point(int p) const { 
        assert(0 <= p && p < N);
        return a[p + N];
    }
    M query_all() const { return a[1]; }
};

struct Event {
    int dist, velo, i;
    int x,y;
    Event() {}
    Event(int _dist, int _velo, int _i, int _x, int _y) : dist(_dist), velo(_velo), i(_i), x(_x), y(_y) {}
    bool operator<(const Event& _oth) const {
        return 1ll*dist * _oth.velo < 1ll*_oth.dist * velo;
    }
};

void solve() {
    int n; cin >> n;
    vector<int> X(n), V(n);
    vector<mint> P(n);
    mint i100 = mint(1)/100;
    for (int i = 0; i < n; i++) {
        cin >> X[i] >> V[i] >> P[i];
        P[i] *= i100;
    }
    using M = array<array<mint,2>,2>;
    vector<M> a(n);
    a[0][0] = {1-P[0],0};
    a[0][1] = {0, P[0]};
    vector<Event> evs;
    for (int i = 1; i < n; i++) {
        a[i][0] = {1-P[i], P[i]};
        a[i][1] = {1-P[i], P[i]};

        int dx = X[i]-X[i-1];
        evs.emplace_back(dx, V[i]+V[i-1], i, 1, 0);
        if (V[i] > V[i-1]) {
            evs.emplace_back(dx, V[i]-V[i-1], i, 0, 0);
        }
        if (V[i] < V[i-1]) {
            evs.emplace_back(dx, V[i-1]-V[i], i, 1, 1);
        }
    }
    sort(evs.begin(), evs.end());

    M ID; ID[0][0]=ID[1][1] = 1;
    auto mul = [](M u, M v){
        M r;
        for (int i:{0,1})
            for (int j:{0,1})
                for (int k:{0,1})
                    r[i][j] += u[i][k] * v[k][j];
        return r;
    };
    auto sum = [](M u){
        mint res = 0;
        for (int i:{0,1})
            for (int j:{0,1})
                res += u[i][j];
        return res;
    };
    SegTree<M> seg(a, ID, mul);

    mint prob = sum(seg.query_all());
    //cout << prob << "\n";
    assert(prob == 1);
    mint res = 0;
    for (auto& e: evs) {
        mint tim = e.dist / mint(e.velo);
        seg.cancel(e.i,e.x,e.y);
        auto nxt = sum(seg.query_all());
        mint hold = prob - nxt; prob = nxt;
        res += hold * tim;
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
