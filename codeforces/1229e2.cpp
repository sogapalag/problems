template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;
using ll=long long;

// alter looper-order could contant-optimized, omit
void solve() {
    int n; cin >> n;
    const int R = 1<<n; 
    vector<int> id(R);
    vector<vector<int>> g(n+1);
    for (int msk = 0; msk < R; msk++) {
        int i = __builtin_popcount(msk);
        id[msk] = g[i].size();
        g[i].emplace_back(msk);
    }
    vector<unordered_map<ll, mint>> dp(n+1); // past i all matched, in a state several matches exist. prob
    dp[0][1<<id[0]] = 1;
    for (int i = 0; i < n; i++) { // extend
        vector<mint> p(n);
        for (auto& x: p) {
            cin >> x; x/=100;
        }
        for (int msk = 0; msk < R; msk++) { // i-R state
            mint prob = 1;
            for (int j = 0; j < n; j++) {
                prob *= (msk>>j&1) ? p[j] : (1-p[j]);
            }
            for (auto& _: dp[i]) {
                ll pm; mint pp; // past_matches_mask: each bit repre. an i-tuple match-to-r, past_prob
                tie(pm,pp) = _;
                ll mates = 0;
                for (int mat: g[i]) {
                    for (int j = 0; j < n; j++) {
                        // i use j,     [..i) don't use j,   such mat is in pm
                        if ((msk>>j&1) && !(mat>>j&1) && (pm>>id[mat]&1)) {
                            mates |= 1ll<<id[mat|1<<j];
                        }
                    }
                }
                dp[i+1][mates] += prob * pp;
            }
        }
    }
    cout << dp[n][1]; // 1 = 1<<id[R-1]
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
