// when range [l, r), has property notP~P, want first P.
// when return r, means not found.
template <typename T>
T bs_first(T l, T r, function<bool (T)> f) {
    assert(l < r);
    T mid;
    while (l != r) {
        mid = l + (r-l)/2;
        if (f(mid)) {
            r = mid;
        }else {
            l = mid + 1;
        }
    }
    return r;
}
using ll=long long;
void solve() {
    int n;cin >> n;
    vector<int> a(n), b(n);
    for (auto& x: a) {
        cin >> x;
    }
    for (auto& x: b) {
        cin >> x;
    }
    Rmq2<> rmx(a);
    Rmq<> rmi(b);
    ll res = 0;
    for (int i = 0; i < n; i++) {
        int l = bs_first<int>(i, n, [&](int x){
                return rmx.query(i, x+1) - rmi.query(i,x+1) >= 0;
                });
        int r = bs_first<int>(i, n, [&](int x){
                return rmx.query(i, x+1) - rmi.query(i,x+1) > 0;
                });
        res += r-l;
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
