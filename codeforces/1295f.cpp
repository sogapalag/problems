    Xs.push_back(-1);
    Xs.push_back(0);
    sort(Xs.begin(), Xs.end());
    Xs.erase(unique(Xs.begin(), Xs.end()), Xs.end());
    int m = Xs.size() - 1;
    vector<pair<int, int>> seg(m);
    for (int i = 0; i < m; i++) {
        seg[i] = {Xs[i], Xs[i+1]};
    }
