template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

struct Tree {
    int n;
    vector<vector<int>> g;
    vector<int> sz, pa, depth, sta, fin, tour;
    int tim;
    Tree(int _n) : n(_n)
        , g(n)
        , sz(n)
        , pa(n, -1)
        , depth(n)
        , sta(n)
        , fin(n)
        {
        tour.reserve(n);
        input(); 
        tim = 0; basic_dfs(0);
    }

    inline void add(int u, int v) {
        g[u].emplace_back(v);
        g[v].emplace_back(u);
    }
    void input() {
        for (int _ = 1; _ < n; _++) {
            int x,y;
            cin >> x >> y;
            --x; --y; // to 0-based
            add(x,y);
        }
    }
    void basic_dfs(int u) {
        if (pa[u] != -1) g[u].erase(find(g[u].begin(), g[u].end(), pa[u]));
        sta[u] = tim++;
        tour.emplace_back(u);
        for (int& v: g[u]) {
            pa[v] = u;
            depth[v] = depth[u] + 1;
            basic_dfs(v);
            sz[u] += sz[v];
        }
        ++sz[u];
        fin[u] = tim;
    }

    // first one can see, a scc must be perfect or is isolated(sz=1)
    // 0: perfect already; 1: must used then; 2: isolated
    // below is a convo-version, 3x3 cases, there's explict formula too. omit.
    void solve() {
        vector<array<mint,3>> dp(n);
        //const int ISO = 2;
        for (int _ = n-1; _ >= 0; _--) {
            int u = tour[_];
            dp[u][2] = 1;
            for (int v:g[u]) {
                mint tmp[3] = {};
                // cut
                for (int i:{0,1,2}) {
                    for (int j:{0,2}) {
                        tmp[i] += dp[u][i] * dp[v][j];
                    }
                }
                // use as match
                for (int i:{1,2}) {
                    for (int j:{1,2}) {
                        tmp[0] += dp[u][i] * dp[v][j];
                    }
                }
                // keep edge, but not match
                tmp[0] += dp[u][0] * dp[v][0];
                tmp[1] += (dp[u][1]+dp[u][2]) * dp[v][0];

                for (int i:{0,1,2}) dp[u][i] = tmp[i];
            }
        }
        cout << dp[0][0] + dp[0][2];
    }
};

void solve() {
    int n; cin >> n;
    Tree tr(n); tr.solve();
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
