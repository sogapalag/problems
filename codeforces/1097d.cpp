template <typename T> inline bool operator!=(const modular<T>& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const modular<T>& _lhs, const U& _rhs) { return !(_lhs == _rhs); }
template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

using ll=long long ;

// p1^e1...p^e , independent.
void solve() {
    ll n; int k;
    cin >> n >> k;
    mint res = 1;
    auto calc = [&](ll p, int e){
        if (e==1) {
            mint prob = (mint(1)/2).exp(k);
            return 1 + (p-1)* prob;
        }
        vector<mint> dist(e+1, 0);
        dist[e] = 1;
        for (int _ = 0; _ < k; _++) {
            for (int i = 1; i <= e; i++) {
                mint avg = dist[i] / (i+1);
                for (int j = 0; j < i; j++) {
                    dist[j] += avg;
                }
                dist[i] = avg;
            }
        }
        mint sum = 0;
        mint prod = 1;
        for (int i = 0; i <= e; i++) {
            sum += prod * dist[i];
            prod *= p;
        }
        return sum;
    };
    for (ll i = 2; i*i <= n; i++) {
        if (n%i) continue;
        int e = 0;
        while (n%i == 0) {
            n /= i;
            e++;
        }
        res *= calc(i, e);
    }
    if (n > 1) res *= calc(n, 1);
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
