// using deque stack method could O(n), omit
void solve() {
    int n, D, L;
    cin >> n >> D >> L;
    vector<int> a(n);
    for (auto& x: a) {
        cin >> x;
    }
    Rmq<> rmi(a);
    Rmq2<> rmx(a);

    SegTree<int> dp(n+1, 1e9, 1e9, [](int u, int v){ return min(u,v); });
    dp.assign(0, 0);

    for (int r = 1, l = 0; r <= n; r++) {
        while (l < r && rmx.query(l,r)-rmi.query(l,r) > D) {
            l++;
        }
        if (r-l >= L) {
            dp.assign(r, dp.query(l, r-L+1) + 1);
        }
    }

    int res = dp.query_point(n);
    if (res > 1e8) res = -1;
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
