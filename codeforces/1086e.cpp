const int N = 2048; 
mint dp[N][N];// generalized derangement, i,j : perm[i] with j constraint, j de. pi!=u
mint pw[N];
int a[N][N];

// it's trivial that total n! (!n)^{n-1} ways.
void solve() {
    int n; cin >> n;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            cin >> a[i][j];
        }
    }
    // prep
    dp[0][0] = 1;
    for (int i = 1; i <= n; i++) {
        dp[i][0] = dp[i-1][0] * i; // n!
    }
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= i; j++) {
            dp[i][j] = dp[i][j-1] - dp[i-1][j-1];
        }
    }
    pw[0] = 1; pw[1] = dp[n][n]; // !n
    for (int i = 2; i <= n; i++) {
        pw[i] = pw[i-1] * pw[1];
    }

    mint res = 0;
    {// first row is kth of n!
        Fenwick<int> f(n);
        for (int j = 1; j <= n; j++) {
            int x = a[1][j];
            int les = (x-1) - f.query(x);
            res += les * dp[n-j][0];
            f.add(x, 1);
        }
        res *= pw[n-1];
    }
    // for each row, with prev-row as de. conditions
    for (int i = 2; i <= n; i++) {
        mint tmp = 0;
        Fenwick<int> de(n), f(n);
        vector<bool> vis(n+1), trk(n+1);
        int cnt = n;
        for (int j = 1; j <= n; j++) {
            int u = a[i-1][j];
            int x = a[i][j];
            int les = (x-1) - f.query(x) - (u<x && !trk[u]);
            int seen = de.query(x-1); // those de occur already
            // note there're two cases, either
            //     y    or  y
            //   y             y 
            // cnt is trigged by first y, thus !other[y]
            // de is trigged by second shape, +,-.
            if (!trk[u]) de.add(u, 1), cnt--;
dbg(les, seen, cnt);
            tmp += seen * dp[n-j][cnt];
            tmp += (les-seen) * dp[n-j][cnt-1]; // !trk[y], like below, thus -1
            // ocuur twice, deactive
            if (vis[x]) de.add(x, -1); else cnt--;
            vis[u] = true; trk[x] = true;
            f.add(x, 1);
dbg(cnt);
        }
        res += tmp * pw[n-i];
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
