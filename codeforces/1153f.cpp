typedef modular<int> mint;

// wlog, l=1, since expectation length, after multiply l is fine.
// first note when 2*n points settled, new point fall into valid(covered>=k) segments's probability is our answer.
// then notice ()().(()), repre segments, '.' is the point.
// note that valid iff left of . has unclosed '('>=k
// note segments always valid '()'string, aka, catalan number.
// but it's a variant catalan. since (()) can repre 1221 or 1212, i.e. when a match happen, has chioces. check out dp block. imagine '(_1..n' in order, select ')_x'
// after dp get the variant catalan, then just add valid i-j>=k's cell to P. #left X #right X (i-j)! (variant catalan)
// , and Q multi 2n+1 since '.' has those chioces. QED.
// 
// another explict formula, imagine point at x, a segment cross it with prob p(x) = 2x(1-x).
// ans is \int_0^1\sum_{i=k..n} (n\choose i) p^i(1-p)^{n-i} dx
void solve() {
    int n, k; mint l;
    cin >> n >> k >> l;
    vector<vector<mint>> dp(n+1, vector<mint>(n+1, 0));
    for (int i = 0; i <= n; i++) {
        dp[i][0] = 1;
    }
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= i; j++) {
            dp[i][j] = dp[i][j-1] * (i-j+1) + dp[i-1][j];
        }
    }
    vector<mint> fac(n+1);
    fac[0] = 1;
    for (int i = 1; i <= n; i++) {
        fac[i] = fac[i-1] * i;
    }
    mint P = 0; mint Q = dp[n][n] * (2*n+1);
    for (int i = k; i <= n; i++) {
        for (int j = 0; j <= i-k; j++) {
            P += dp[i][j] * dp[n-j][n-i] * fac[i-j];
        }
    }
    mint res = P/Q * l;
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
