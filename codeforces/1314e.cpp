template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

using vi=vector<int>;
// k=1, valid iff sum(a)<=n
// k=2, valid iff sum(b*i) <= n, {i} unique
// suppose a,b sorted-dec, note 1 unit dx contribution at i, is i.
// for k=2, note we just wanna valid b, thus (i)=1,2,3... the sum is minimized.
//   thus 1 unit dx contribution to sum, is (1+2+..+i) = i*(i+1)/2
// one can see k=2, len ~ sqrt(n), thus future res is small
// so k>=3 just brute-force check by reversal(unfolding)
//   also, the unfolding process just greedy assign reverse-sort i with x.
void solve() {
    int n,m;
    cin >> n >> m;
    if (m <= 2) {
        vector<mint> dp(n+1);
        dp[0] = 1;
        for (int i = 1; i <= n; i++) {
            int coeff = m==1 ? i : i*(i+1)/2;
            if (coeff > n) break;
            for (int s = 0; s+coeff <= n; s++) {
                dp[s+coeff] += dp[s];
            }
        }
        mint res = 0;
        for (int s = 1; s <= n; s++) {
            res += dp[s];
        }
        cout << res << "\n"; return;
    }
    auto ck = [&](vi a){
        for (int _ = 0; _ < m-1; _++) {
            vi b;
            int sum = 0;
            int i = a.size();
            for (int x:a) {
                sum += i*x;
                if (sum > n) return false;
                for (int __ = 0; __ < x; __++) {
                    b.push_back(i);
                }
                --i;
            }
            reverse(b.begin(), b.end());
            a = b;
        }
        return true;
    };

    int res = 0;
    function<void(vi)> dfs = [&](vi a) {
        if (!ck(a)) return;
        ++res;
        ++a.back(); dfs(a); --a.back();
        int x = a.back();
        a.push_back(x); dfs(a); a.pop_back();
    };
    dfs({1});
    cout << res << "\n";
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
