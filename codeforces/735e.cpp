typedef modular<int> mint;

const int N = 111; 
const int K = 24 * 2; 
mint dp[N][K], convo[K];

// editorial method, maintain closest black, furest white. O(nk^4)
//   key is focus change, merge doesn't imply all sub valid. as long as maintain info for future.
// here O(nk^2) is note that we only need maintain 
//       1. cloest black, when all sub valid.
//   or  2. furest white unsats.
//  why? one might thought what if Wv sats by Bv', but v' also has unsats W, which is not include above.
//    note in this case, Wv' must has larger depth than Wv, so v' is case.2 enough.
// below impl use a compressed-way, 0<=i<=k is case.1,  k<i is case.2
void solve() {
    int n,k;
    cin >> n >> k;
    vector<vector<int>> g(n);
    for (int _ = 1; _ < n; _++) {
        int x,y;
        cin >> x >> y;
        x--;y--;
        g[x].push_back(y);
        g[y].push_back(x);
    }

    function<void(int,int)> dfs = [&](int u, int p){
        dp[u][0] = 1;
        dp[u][k+1] = 1;
        for (int v:g[u])if(v!=p){
            dfs(v,u);
            memset(convo, 0, sizeof convo);
            for (int i = 0; i <= 2*k; i++) {
                for (int j = 0; j <= 2*k; j++) {
                    // a clear way is write (1.2.)x(1.2.) 4 cases.
                    int x = i+j+1 > 2*k+1 ? max(i,j+1) : min(i,j+1); 
                    convo[x] += dp[u][i] * dp[v][j];
                }
            }
            memcpy(dp[u], convo, sizeof dp[u]);
        }
    };
    dfs(0,-1);
    mint res = 0;
    for (int i = 0; i <= k; i++) {
        res += dp[0][i];
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
