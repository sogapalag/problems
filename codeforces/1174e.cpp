template <typename T, typename U> inline bool operator!=(const U& _lhs, const modular<T>& _rhs) { return !(_lhs == _rhs); }
typedef modular<int> mint;

const int N = 1e6+6; 
const int L = 22; 

int f[L][2];
mint dp[N][L][2];


// 2^x or 2^{x-1}3 (<=n)
// space can be optimized.
// also, a binomial way faster
void solve() {
    int n; cin >> n;
    for (int i = 1; i <= n; i++) {
        for (int x = 0; x < L; x++) {
            if (i % (1<<x) == 0) {
                f[x][0]++;
                if (i%3 == 0)
                    f[x][1]++;
            }
        }
    }
    int xx = 31 - __builtin_clz(n);
    dp[1][xx][0] = 1;
    if (xx>0 && (1<<(xx-1))*3 <= n)
        dp[1][xx-1][1] = 1;
    for (int i = 1; i < n; i++) {
        for (int x = 0; x < L; x++) {
            for (int y = 0; y < 2; y++) {
                dp[i+1][x][y] += dp[i][x][y] * max(0, f[x][y] - i);
                if (x>0)
                    dp[i+1][x-1][y] += dp[i][x][y] * (f[x-1][y] - f[x][y]);
                if (y>0)
                    dp[i+1][x][y-1] += dp[i][x][y] * (f[x][y-1] - f[x][y]);
            }
        }
    }
    cout << dp[n][0][0];
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    cout << endl;
}
