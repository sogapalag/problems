using poly = deque<mint>;// well, queue is enough
// in poly's view the relation is easy to impl.
//   since [len][k]->[len+1][k], len>=1, i.e. a shift
//    and  [len][y]->[1][x], any y!=x, thus we need keep sum
void solve() {
    int n,k,lim;
    cin >> n >> k >> lim;
    vector<poly> dp(k);
    vector<mint> sum(k); //  sum.. dp[i][..]
    for (int _ = 0; _ < n; _++) {
        mint tot = _?0:1; // empty
        int x; cin >> x; x--;
dbg(_,x);
        if (x < 0) {
            for (int i = 0; i < k; i++) {
                tot += sum[i];
            }
            for (int i = 0; i < k; i++) {
                mint one = tot - sum[i];
                dp[i].push_front(one); sum[i] += one;
                if (dp[i].size() >= lim)   sum[i] -= dp[i].back(), dp[i].pop_back();
dbg(i,dp[i]);
            }
        } else {
            for (int i = 0; i < k; i++) {
                tot += sum[i];
                if (i!=x) dp[i].clear(), sum[i] = 0;
            }
            mint one = tot - sum[x];
            dp[x].push_front(one); sum[x] += one;
            if (dp[x].size() >= lim) sum[x] -= dp[x].back(), dp[x].pop_back();
dbg(dp[x]);
        }
    }
    mint res = 0;
    for (int i = 0; i < k; i++) {
        res += sum[i];
    }
    cout << res;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    solve();
    return 0;
}
